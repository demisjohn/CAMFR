#include "bessel.h"

/////////////////////////////////////////////////////////////////////////////
//
// Benchmark for bessel routines
//
/////////////////////////////////////////////////////////////////////////////

int main()
{ 
  int n = 1;
  
  for (Real x=0.1; x<=10.0; x+=0.1)
    for (Real y=0.1; y<=10.0; y+=0.1)
    {
      Complex z(x,y);

      dJ(n, z);
      dY(n, z);
      dH1(n, z);
      dH2(n, z);
    }
  
  return 0;
}

